#!/bin/bash

# Free Database Migration Script for Filess.io
# Migrates existing gemsdb to Filess.io free hosting

# Colors for output
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

echo -e "${BLUE}üöÄ Free Database Migration Tool for gemsdb ‚Üí Filess.io${NC}"
echo -e "${BLUE}üíé Optimized for jewelry store database migration${NC}"
echo ""

# Step 1: Check if mysqldump is available
echo -e "${YELLOW}üîç Checking system requirements...${NC}"
if ! command -v mysqldump &> /dev/null; then
    echo -e "${RED}‚ùå mysqldump is not installed. Please install MySQL client tools.${NC}"
    echo "Ubuntu/Debian: sudo apt-get install mysql-client"
    echo "macOS: brew install mysql-client"
    echo "Windows: Download MySQL installer from mysql.com"
    exit 1
fi

if ! command -v mysql &> /dev/null; then
    echo -e "${RED}‚ùå mysql client is not installed. Please install MySQL client tools.${NC}"
    exit 1
fi

echo -e "${GREEN}‚úÖ MySQL client tools found${NC}"

# Step 2: Get local database credentials
echo -e "${YELLOW}üìù Local Database Connection Details${NC}"
echo "Please provide your local gemsdb connection details:"
echo ""

read -p "Local MySQL Host (default: localhost): " LOCAL_HOST
LOCAL_HOST=${LOCAL_HOST:-localhost}

read -p "Local MySQL Port (default: 3306): " LOCAL_PORT
LOCAL_PORT=${LOCAL_PORT:-3306}

read -p "Local MySQL Username (default: root): " LOCAL_USER
LOCAL_USER=${LOCAL_USER:-root}

read -s -p "Local MySQL Password: " LOCAL_PASSWORD
echo ""

read -p "Local Database Name (default: gemsdb): " LOCAL_DB
LOCAL_DB=${LOCAL_DB:-gemsdb}

# Step 3: Test local connection
echo -e "${YELLOW}üîó Testing local database connection...${NC}"
if mysql -h"$LOCAL_HOST" -P"$LOCAL_PORT" -u"$LOCAL_USER" -p"$LOCAL_PASSWORD" -e "USE $LOCAL_DB; SELECT 1;" &> /dev/null; then
    echo -e "${GREEN}‚úÖ Local database connection successful${NC}"
else
    echo -e "${RED}‚ùå Failed to connect to local database. Please check your credentials.${NC}"
    exit 1
fi

# Step 4: Create export directory
EXPORT_DIR="filess_export_$(date +%Y%m%d_%H%M%S)"
mkdir -p "$EXPORT_DIR"
echo -e "${YELLOW}üìÅ Created export directory: $EXPORT_DIR${NC}"

# Step 5: Export database schema
echo -e "${YELLOW}üóÑÔ∏è Exporting database schema...${NC}"
mysqldump -h"$LOCAL_HOST" -P"$LOCAL_PORT" -u"$LOCAL_USER" -p"$LOCAL_PASSWORD" \
    --no-data \
    --single-transaction \
    --lock-tables=false \
    --add-drop-table \
    --skip-comments \
    --skip-set-charset \
    --skip-extended-insert \
    "$LOCAL_DB" > "$EXPORT_DIR/original_schema.sql"

if [ $? -eq 0 ]; then
    echo -e "${GREEN}‚úÖ Original schema exported successfully${NC}"
else
    echo -e "${RED}‚ùå Failed to export schema${NC}"
    exit 1
fi

# Step 6: Export database data
echo -e "${YELLOW}üìä Exporting database data...${NC}"
mysqldump -h"$LOCAL_HOST" -P"$LOCAL_PORT" -u"$LOCAL_USER" -p"$LOCAL_PASSWORD" \
    --no-create-info \
    --single-transaction \
    --lock-tables=false \
    --skip-comments \
    --skip-set-charset \
    --complete-insert \
    "$LOCAL_DB" > "$EXPORT_DIR/original_data.sql"

if [ $? -eq 0 ]; then
    echo -e "${GREEN}‚úÖ Original data exported successfully${NC}"
else
    echo -e "${RED}‚ùå Failed to export data${NC}"
    exit 1
fi

# Step 7: Create Filess.io-compatible schema
echo -e "${YELLOW}üîß Creating Filess.io-compatible schema...${NC}"

cat > "$EXPORT_DIR/filess_schema.sql" << 'EOF'
-- Filess.io compatible schema for gemsdb
-- Optimized for free hosting with 100MB limit
-- Generated by Filess.io migration script

SET foreign_key_checks = 0;
SET sql_mode = 'NO_AUTO_VALUE_ON_ZERO';

-- Users table
CREATE TABLE IF NOT EXISTS users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    email VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    first_name VARCHAR(100),
    last_name VARCHAR(100),
    role ENUM('customer', 'admin') DEFAULT 'customer',
    phone VARCHAR(20),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_email (email),
    INDEX idx_role (role)
);

-- Products table
CREATE TABLE IF NOT EXISTS products (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    description TEXT,
    price DECIMAL(10, 2) NOT NULL,
    category ENUM('rings', 'necklaces', 'bracelets', 'earrings', 'gemstones', 'other') NOT NULL,
    stock INT DEFAULT 0,
    image_url VARCHAR(500),
    featured BOOLEAN DEFAULT FALSE,
    weight DECIMAL(8, 2),
    material VARCHAR(100),
    gemstone VARCHAR(100),
    carat DECIMAL(8, 2),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_category (category),
    INDEX idx_featured (featured),
    INDEX idx_price (price)
);

-- Orders table
CREATE TABLE IF NOT EXISTS orders (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    order_number VARCHAR(50) UNIQUE,
    total_amount DECIMAL(10, 2) NOT NULL,
    status ENUM('pending', 'processing', 'shipped', 'delivered', 'cancelled') DEFAULT 'pending',
    payment_status ENUM('pending', 'paid', 'failed', 'refunded') DEFAULT 'pending',
    shipping_address TEXT,
    billing_address TEXT,
    notes TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_user_id (user_id),
    INDEX idx_status (status),
    INDEX idx_order_number (order_number)
);

-- Order items table
CREATE TABLE IF NOT EXISTS order_items (
    id INT AUTO_INCREMENT PRIMARY KEY,
    order_id INT NOT NULL,
    product_id INT NOT NULL,
    quantity INT NOT NULL,
    price DECIMAL(10, 2) NOT NULL,
    total_price DECIMAL(10, 2) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_order_id (order_id),
    INDEX idx_product_id (product_id)
);

-- Customer addresses table
CREATE TABLE IF NOT EXISTS customer_addresses (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    address_type ENUM('shipping', 'billing') DEFAULT 'shipping',
    address_line1 VARCHAR(255) NOT NULL,
    address_line2 VARCHAR(255),
    city VARCHAR(100) NOT NULL,
    state VARCHAR(100),
    postal_code VARCHAR(20),
    country VARCHAR(100) NOT NULL DEFAULT 'US',
    is_default BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_user_id (user_id),
    INDEX idx_address_type (address_type)
);

-- Product images table
CREATE TABLE IF NOT EXISTS product_images (
    id INT AUTO_INCREMENT PRIMARY KEY,
    product_id INT NOT NULL,
    image_url VARCHAR(500) NOT NULL,
    alt_text VARCHAR(255),
    is_primary BOOLEAN DEFAULT FALSE,
    sort_order INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_product_id (product_id),
    INDEX idx_is_primary (is_primary)
);

-- Reviews table
CREATE TABLE IF NOT EXISTS reviews (
    id INT AUTO_INCREMENT PRIMARY KEY,
    product_id INT NOT NULL,
    user_id INT NOT NULL,
    rating INT NOT NULL CHECK (rating >= 1 AND rating <= 5),
    title VARCHAR(255),
    comment TEXT,
    verified_purchase BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_product_id (product_id),
    INDEX idx_user_id (user_id),
    INDEX idx_rating (rating)
);

-- Categories table
CREATE TABLE IF NOT EXISTS categories (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    description TEXT,
    parent_id INT DEFAULT NULL,
    image_url VARCHAR(500),
    is_active BOOLEAN DEFAULT TRUE,
    sort_order INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_parent_id (parent_id),
    INDEX idx_is_active (is_active)
);

-- Coupons table
CREATE TABLE IF NOT EXISTS coupons (
    id INT AUTO_INCREMENT PRIMARY KEY,
    code VARCHAR(50) NOT NULL UNIQUE,
    discount_type ENUM('percentage', 'fixed') NOT NULL,
    discount_value DECIMAL(10, 2) NOT NULL,
    min_order_amount DECIMAL(10, 2) DEFAULT 0,
    max_uses INT DEFAULT NULL,
    used_count INT DEFAULT 0,
    expires_at TIMESTAMP NULL,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_code (code),
    INDEX idx_is_active (is_active)
);

-- Inventory log table
CREATE TABLE IF NOT EXISTS inventory_log (
    id INT AUTO_INCREMENT PRIMARY KEY,
    product_id INT NOT NULL,
    change_type ENUM('stock_in', 'stock_out', 'adjustment') NOT NULL,
    quantity_change INT NOT NULL,
    old_quantity INT NOT NULL,
    new_quantity INT NOT NULL,
    reason VARCHAR(255),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_product_id (product_id),
    INDEX idx_change_type (change_type)
);

SET foreign_key_checks = 1;
EOF

# Step 8: Clean up data for free hosting
echo -e "${YELLOW}üßπ Cleaning up data for free hosting...${NC}"

# Create a clean version of the data for import
sed -e '/^\/\*!/d' \
    -e '/^--/d' \
    -e '/^$/d' \
    -e 's/`//g' \
    -e '/SET.*foreign_key_checks/d' \
    -e '/SET.*sql_mode/d' \
    -e '/SET.*time_zone/d' \
    -e '/SET.*character_set_client/d' \
    -e '/SET.*character_set_results/d' \
    -e '/SET.*collation_connection/d' \
    -e '/LOCK TABLES/d' \
    -e '/UNLOCK TABLES/d' \
    -e '/AUTO_INCREMENT=/d' \
    "$EXPORT_DIR/original_data.sql" > "$EXPORT_DIR/filess_data.sql"

